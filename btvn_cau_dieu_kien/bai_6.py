n = int(input("Nhập thứ tự phim bạn muốn xem:"))
if n == 1:
    print('bạn đã chọn phim tình cảm')
elif n == 2:
    print('bạn đã chọn phim kinh dị')
elif n == 3:
    print('bạn đã chọn phim hoạt hình')
elif n == 4:
    print('bạn đã chọn phim khoa học viễn tưởng')
else:
    print('hãy chọn lại phim')